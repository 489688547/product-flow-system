import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { pathToFileURL } from "node:url";

const ROOT = process.cwd();
const ENVIRONMENT_PATH = "docs/platform/environment-capabilities.json";
const REGISTRY_PATH = "docs/platform/integration-registry.json";
const TARGETS = [
  {
    source: ENVIRONMENT_PATH,
    target: "functions/api/platform/_generated/environmentCapabilities.js",
    exportName: "environmentCapabilities"
  },
  {
    source: REGISTRY_PATH,
    target: "functions/api/platform/_generated/integrationRegistry.js",
    exportName: "integrationRegistry"
  }
];

const CAPABILITY_FIELDS = new Set([
  "id",
  "name",
  "description",
  "platforms",
  "requiredIn",
  "level",
  "envVars",
  "bindings",
  "tables",
  "bindingTables"
]);
const ENVIRONMENTS = new Set(["development", "preview", "production"]);
const LEVELS = new Set(["blocking", "warning"]);
const IDENTIFIER = /^[A-Z][A-Z0-9_]*$/;
const SLUG = /^[a-z0-9]+(?:-[a-z0-9]+)*$/;
const SECRET_VALUE = /(?:=|Bearer\s|-----BEGIN|\bsk-)/i;

function stringList(value) {
  return Array.isArray(value) && value.every(item => typeof item === "string" && item.trim());
}

export function validateEnvironmentCapabilities(manifest, registry) {
  const errors = [];
  if (manifest?.schemaVersion !== 1) errors.push("环境能力 schemaVersion 必须是 1");
  if (!/^\d{4}-\d{2}-\d{2}$/.test(manifest?.updatedAt || "")) errors.push("环境能力 updatedAt 必须是 YYYY-MM-DD");
  if (!Array.isArray(manifest?.capabilities) || !manifest.capabilities.length) {
    errors.push("环境能力 capabilities 必须是非空数组");
    return errors;
  }

  const platformIds = new Set((registry?.platforms || []).map(platform => platform.id));
  const capabilityIds = new Set();
  manifest.capabilities.forEach((capability, index) => {
    const context = `capabilities[${index}]`;
    if (!capability || typeof capability !== "object" || Array.isArray(capability)) {
      errors.push(`${context} 必须是对象`);
      return;
    }
    Object.keys(capability).filter(key => !CAPABILITY_FIELDS.has(key))
      .forEach(key => errors.push(`${context} 包含未知字段：${key}`));
    if (!SLUG.test(capability.id || "")) errors.push(`${context}.id 格式无效`);
    if (capabilityIds.has(capability.id)) errors.push(`环境能力 ID 重复：${capability.id}`);
    capabilityIds.add(capability.id);
    if (!capability.name?.trim()) errors.push(`${context}.name 不能为空`);
    if (!capability.description?.trim()) errors.push(`${context}.description 不能为空`);
    if (!stringList(capability.platforms) || !capability.platforms.length) errors.push(`${context}.platforms 必须是非空字符串数组`);
    else capability.platforms.filter(id => !platformIds.has(id))
      .forEach(id => errors.push(`${context} 引用了未知平台：${id}`));
    if (!stringList(capability.requiredIn) || capability.requiredIn.some(value => !ENVIRONMENTS.has(value))) {
      errors.push(`${context}.requiredIn 包含无效环境`);
    }
    if (!LEVELS.has(capability.level)) errors.push(`${context}.level 必须是 blocking 或 warning`);
    for (const field of ["envVars", "bindings", "tables"]) {
      if (!Array.isArray(capability[field]) || !capability[field].every(item => typeof item === "string" && item.trim())) {
        errors.push(`${context}.${field} 必须是字符串数组`);
      }
    }
    if (capability.bindingTables !== undefined) {
      if (!capability.bindingTables || typeof capability.bindingTables !== "object" || Array.isArray(capability.bindingTables)) {
        errors.push(`${context}.bindingTables 必须是绑定到表名数组的对象`);
      } else {
        for (const [binding, tables] of Object.entries(capability.bindingTables)) {
          if (!IDENTIFIER.test(binding) || !(capability.bindings || []).includes(binding)) {
            errors.push(`${context}.bindingTables 包含未声明绑定：${binding}`);
          }
          if (!stringList(tables)) {
            errors.push(`${context}.bindingTables.${binding} 必须是字符串数组`);
          }
        }
      }
    }
    for (const variable of [...(capability.envVars || []), ...(capability.bindings || [])]) {
      if (!IDENTIFIER.test(variable) || SECRET_VALUE.test(variable)) errors.push(`${context} 变量名无效或疑似包含值：${variable}`);
    }
  });
  return errors;
}

export function renderGeneratedModule(exportName, value) {
  return `// Generated by scripts/generate-platform-manifests.mjs. Do not edit.\nconst ${exportName} = ${JSON.stringify(value, null, 2)};\n\nexport default ${exportName};\n`;
}

function loadJson(path) {
  return JSON.parse(readFileSync(resolve(ROOT, path), "utf8"));
}

export function generatePlatformManifestModules({ check = false } = {}) {
  const manifest = loadJson(ENVIRONMENT_PATH);
  const registry = loadJson(REGISTRY_PATH);
  const errors = validateEnvironmentCapabilities(manifest, registry);
  if (errors.length) throw new Error(errors.join("\n"));

  const outputs = TARGETS.map(item => {
    const content = renderGeneratedModule(item.exportName, item.source === ENVIRONMENT_PATH ? manifest : registry);
    const target = resolve(ROOT, item.target);
    if (check) {
      if (!existsSync(target) || readFileSync(target, "utf8") !== content) {
        throw new Error(`生成模块已漂移：${item.target}，请运行 npm run generate:platform-manifests`);
      }
    } else {
      mkdirSync(dirname(target), { recursive: true });
      writeFileSync(target, content);
    }
    return item.target;
  });
  return outputs;
}

if (process.argv[1] && pathToFileURL(resolve(process.argv[1])).href === import.meta.url) {
  const check = process.argv.includes("--check");
  const outputs = generatePlatformManifestModules({ check });
  process.stdout.write(`${check ? "平台生成模块检查通过" : "已生成平台模块"}：${outputs.join("、")}\n`);
}
